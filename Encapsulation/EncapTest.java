public class EncapExample {
    private String name;
    private String idNum;
    private int age;

    public int getAge() { // getter method
        return age;
    }

    public int getName() {
        return name;
    }

    public getIdNum() {
        return idNum;
    }

    public setAge(int newAge) { // setter method
        age = newAge;
    }

    public setName(String newName) {
        name = newName;
    }

    public setIdNum(String newId) {
        idNum = newId;
    }
}

